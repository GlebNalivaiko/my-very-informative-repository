public class Numders {
    public static void main(String[] args) {
        int myInt = 10000;
        byte myByte = 100;
        long myLong = 101;
        double myDouble = 10.5;
        float myFloat = 100.5f;
        System.out.println(myInt + myByte);
        System.out.println(myInt / myByte);
        System.out.println(myDouble + myFloat);
        System.out.println(myLong % myByte);
    }
}
